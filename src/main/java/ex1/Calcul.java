package ex1;

public class Calcul {

	/** fonction additionner
	 * @param a : int
	 * @param b : int
	 * @return a+b : int
	 */
	public int additioner(int a, int b){
		return a+b;
	}
	
	/** fonction soustraire
	 * @param a : int
	 * @param b : int
	 * @return a-b : int
	 */
	public int soustraire(int a, int b){
		return a-b;
	}
}
